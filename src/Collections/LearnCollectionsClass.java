package Collections;

public class LearnCollectionsClass {
    /*
        Collections is the class in java which resides in util package.
        This class consists exclusively of static methods that operate on or return collections.

    */

    /*
        Methods of collections class :
        public static void sort(List list);
        public static void sort(List list, comparator c);
        public static int binarySearch(List list, T key);
        public static int binarySearch(List list, T key , comparator c);
        public static void reverse(List list);
        public static void shuffle(List list);
        public static void swap(List list, int i, int j);
        public static void copy(List dest, List src);
        public static T min(Collection c);
        public static T min(Collection c, comparator c);
        public static T max(Collection c);
        public static T max(Collection c, comparator c);



     */

    /*
        Arrays class :
        It resides in util package
        This class contains various static methods for manipulating arrays.

        public static void sort(int[] a);
        public static void sort(int[] a, int fromIndex, int toIndex);
        public static void sort(long[] a);
        public static void sort(long[] a, int fromIndex, int toIndex);
        ... polymorphic versions of sort()

        public static int binarySearch(long[] a, long key);
        public static int binarySearch(long[] a, int fromIndex, int toIndex, long key);
        .. polymorphic versions of binarySearch

        public static boolean equals (int[] a, int[] b);
        .. polymorphic versions

        public static void fill(int[] a, int val);
        public static void fill(int[] a, int fromIndex, int toIndex, int val);
        .. polymorphic versions
    */
}
